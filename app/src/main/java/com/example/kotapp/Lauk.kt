package com.example.kotapp

open class Lauk {
    var namaLauk:String
    var keterangan:String
    var detail:String
    var harga:Int
    var gambar:Int

    constructor(namaLauk: String, keterangan: String, detail: String, harga: Int, gambar: Int) {
        this.namaLauk = namaLauk
        this.keterangan = keterangan
        this.detail = detail
        this.harga = harga
        this.gambar = gambar
    }

}